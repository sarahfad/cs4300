def integer_num():
    """Function returns an integer """
    return 20

def float_num():
    """Function returns a floating point number """
    return 2.12

def strings():
    """Function returns a string """
    return "Hello, World!"

def boolean():
    """ Function returns a boolean value """
    return True


if __name__ == "__main__":
    print(f"integer: {integer_num()}, float: {float_num()}, string: {strings()}, boolean: {boolean()}")